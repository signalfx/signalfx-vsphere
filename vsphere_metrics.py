def get_metrics(conf):
    vsphere_metrics = list()
    vsphere_metrics.extend(metrics['basic_metrics'])
    if 'enhanced_metrics' in conf:
        vsphere_metrics.extend(metrics['enhanced_metrics'])
    if 'include_metrics' in conf:
        for metric in conf['include_metrics']:
            vsphere_metrics.append(metric)
